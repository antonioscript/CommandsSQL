--COMANDO NOLOCK, ORDER BY E DESC 
SELECT * FROM VENDAS(NOLOCK) --Pega todas as informações que já foram commitadas
ORDER BY VALOR_TOTAL DESC


--COMANDO COUNT
SELECT COUNT(*) AS QTDE FROM VENDAS


--COMANDO LEN
SELECT
DESCRICAO,
VALOR,
LEN(DESCRICAO) AS TAMANHO_DESCRICAO
FROM PRODUTOS


--COMANDO IS NULL
SELECT
A.ID_CLIENTE,
A.NOME,
ISNULL(QUANTIDADE,0) AS QUANTIDADE,
ISNULL(VALOR_TOTAL,0) AS VALOR_TOTAL
FROM CLIENTES A(NOLOCK)
LEFT JOIN VENDAS B(NOLOCK) ON (B.ID_CLIENTE = A.ID_CLIENTE)


--COMANDO TOP
SELECT TOP 5 * FROM PRODUTOS


--COMANDO CAST
SELECT 
DESCRICAO,
CAST(VALOR AS VARCHAR(20)) AS VALOR
FROM PRODUTOS


--COMANDO WHERE
